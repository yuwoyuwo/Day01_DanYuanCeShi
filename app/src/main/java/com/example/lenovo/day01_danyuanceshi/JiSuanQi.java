public class JiSuanQi {
    public int jia(int a,int b){
        return a+b;
    }

    public int jian(int a,int b){
        return a-b;
    }

    public int cheng(int a,int b){
        return a*b;
    }

    public int chu(int a,int b){
        return a/b;
    }
}
